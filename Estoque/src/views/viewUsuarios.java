    private void preencherCampos(Usuario objUsuario)
    {
        
        txtNome.setText(objUsuario.getNome());
        objComboStatus.SetaComboBox(String.valueOf(objUsuario.getStatus()));
        txtLogin.setText(objUsuario.getLogin());
        txtSenha.setText(Criptografia.Encriptar(objUsuario.getSenha()));
    }
    
    private void limparCampos()
    {
       
        txtNome.setText("");
        objComboStatus.SetaComboBox("");
        txtLogin.setText("");
        txtSenha.setText("");
        
        jcbStatus.setSelectedIndex(0);
        jcbStatus.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnInativar.setEnabled(false);
        btnExcluir.setEnabled(false);
        jcbInativos.setSelected(false);
        type=checkboxInativar();
        atualizarTabela(type);
    }
    
    private void atualizarTabela(int type) 
    {
        try 
        {
            usuariosController usuarioCon = new usuariosController(null, jtbUsuarios);
            usuarioCon.preencherTabela(type,0,null);
            countRegistros();
        } catch (Exception ex) {
            CaixaDeDialogo.obterinstancia().exibirMensagem("ERRO:" + ex.getMessage());
        }
    }
    
